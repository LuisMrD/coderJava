package Streamss;

public class Pokemon {

    public final String nome;
    public final String tipo;
    public final int estagioEvolucao;

    public Pokemon(String nome, String tipo, int estagioEvolucao) {
        this.nome = nome;
        this.tipo = tipo;
        this.estagioEvolucao = estagioEvolucao;
    }
}
